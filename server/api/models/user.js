const mongoose = require('mongoose');
const { Schema } = mongoose;

const schema = new Schema(
  {
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    firstName: {
      type: String,
      required: true,
    },
    lastName: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const model = mongoose.model('User', schema);

module.exports = model;
